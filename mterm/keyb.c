int KeyDriver( char c, char ec );
static int KeyDriver( c, ec )
    char c, ec;         /* Primary & extension ASCII characters */
{
    int rv;

    rv = c & 0xFF;
    if( c == 0 ){                       /* Extended ASCII code */
	if(      ec == 0x03 ) rv = 0;       /* Debugger Call */
	else if( ec == 0x0F ) rv = LF;      /* Shitf+TAB     */
	else if( ec == 0x52 ) rv = IC;      /* Ins  */
        else if( ec == 0x53 ) rv = DC;      /* Del  */
        else if( ec == 0x47 ) rv = HO;      /* Home */
        else if( ec == 0x49 ) rv = IL;      /* PgUp */
        else if( ec == 0x4F ) rv = RN;      /* End  */
        else if( ec == 0x51 ) rv = DL;      /* PgDn */
        else if( ec == 0x4B ) rv = CL;
        else if( ec == 0x4D ) rv = CR;
        else if( ec == 0x48 ) rv = CU;
        else if( ec == 0x50 ) rv = CD;
        else if( ec == 0x3B ) rv = 0x13;    /* F1   */
	else if( ec == 0x3C ) rv = PR;      /* F2                      */
	else if( ec == 0x3D ) rv = ER;      /* F3                      */
	else if( ec == 0x3F ) rv = 0xEA;    /* F5   --> DEL=PtoM[0xEA] */
	else if( ec == 0x58 ) rv = ER;      /* F12                     */
	else if( ec == 0x42 ) rv = X_ON;    /* F8   -- manual START    */
	else if( ec == 0x43 ){              /* F9   -- call SetUp      */
	    SetUp();
	    rv = 0x100;
	}else if(ec == 0x44 ) Stop( 0 );    /* F10                     */
	else if( ec == 0x70 ){              /* Alt+F9                  */
	    scrmode = (scrmode+1) % maxmode;
	    NewSize( scrmode, 1 );
	    ExtPict();
	    rv = 0x100;
	}
    }else{                              /* Simple recoding */
	if(      c == 0x03 ) Stop( 0 );     /* Ctrl+C                  */
	else if( c == 0x1B ){               /* Esc / Clrl+[            */
	    if( ec == 0x01 ) rv = HO; else rv = 0x1B;
	}else if(c == 0x0D ) rv = ET;       /* Enter                   */
	else if( c == 0x08 ) rv = LF;       /* <-- BS                  */
    }
    return rv;
}

/*
 * Tick entry for keyboard task
 */
void
Keyb()
{
    static int modcnt;
    extern int Kdpm( void );

    int c;
    register int rawc;

    if( (modcnt = (modcnt+1) % 4) != 0 ) return;

    if( (rawc = Kdpm()) >= 0 ){
	c = KeyDriver( rawc, rawc >> 8 );
	if( (c & 0x100) == 0 ) OutQue( PCalt_to_MISS[ c ] );
    }

    Input();

    return;
}

/*
 * Sincronous keyboard reading for SetUp
 */
char dpi()
{
    union REGS rint;

    rint.h.ah = _KEYBRD_READ;
    int86( 0x16, &rint, &rint );
    return KeyDriver( rint.h.al, rint.h.ah );
}
